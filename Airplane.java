public class Airplane {

  /**
  * The placeholder for the speed.
  */
  private int speed;

  /**
  * Empty initializer.
  */
  public Airplane() {
  }

  /**
  * The setSpeed method.
  *
  * @param newSpeed the new speed.
  */
  public void setSpeed(final int newSpeed) {
    this.speed = newSpeed;
  }

  /**
  * The getSpeed method.
  *
  * @return speed.
  */
  public int getSpeed() {
    return speed;
  }
}
